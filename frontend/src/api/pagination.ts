// Laravel's default paginator JSON shape (LengthAwarePaginator::toArray()).
// Every paginated list endpoint in this backend returns this envelope; the
// array of records itself always lives under `.data`.
export interface Paginated<T> {
  current_page: number;
  data: T[];
  first_page_url: string | null;
  from: number | null;
  last_page: number;
  last_page_url: string | null;
  links: { url: string | null; label: string; active: boolean }[];
  next_page_url: string | null;
  path: string;
  per_page: number;
  prev_page_url: string | null;
  to: number | null;
  total: number;
}
